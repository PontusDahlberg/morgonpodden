#!/usr/bin/env python3
"""
üîê API Key Backup Manager f√∂r M√§nniska Maskin Milj√∂
Skapar s√§kra backupper av dina API-nycklar och hj√§lper dig √•terst√§lla dem.
"""

import os
import json
import shutil
from datetime import datetime
from pathlib import Path
import getpass

class APIKeyBackupManager:
    def __init__(self):
        self.project_root = Path(__file__).parent
        self.env_file = self.project_root / ".env"
        self.backup_dir = self.project_root / "backups" / "credentials"
        self.backup_dir.mkdir(parents=True, exist_ok=True)
        
    def create_backup(self):
        """Skapa en s√§ker backup av API-nycklar"""
        print("üîê SKAPAR S√ÑKER BACKUP AV API-NYCKLAR")
        print("=" * 50)
        
        # S√ÑKERHETSKONTROLL: Verifiera att backups ignoreras av git
        self._verify_git_ignore_security()
        
        if not self.env_file.exists():
            print("‚ùå .env fil saknas!")
            return
            
        # L√§s .env filen
        env_vars = {}
        with open(self.env_file, 'r', encoding='utf-8') as f:
            for line in f:
                line = line.strip()
                if line and not line.startswith('#') and '=' in line:
                    key, value = line.split('=', 1)
                    env_vars[key.strip()] = value.strip()
        
        # Identifiera API-nycklar
        api_keys = {}
        sensitive_patterns = [
            'API_KEY', 'TOKEN', 'SECRET', 'CREDENTIAL', 'PASSWORD'
        ]
        
        for key, value in env_vars.items():
            if any(pattern in key.upper() for pattern in sensitive_patterns):
                if value and value != 'your_' + key.lower() + '_here':
                    api_keys[key] = value
        
        if not api_keys:
            print("‚ö†Ô∏è Inga API-nycklar hittades att s√§kerhetskopiera")
            return
            
        # Skapa timestampad backup
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        backup_file = self.backup_dir / f"api_keys_backup_{timestamp}.json"
        
        backup_data = {
            "timestamp": timestamp,
            "datetime": datetime.now().isoformat(),
            "project": "M√§nniska Maskin Milj√∂",
            "api_keys": api_keys,
            "env_file_backup": str(self.env_file)
        }
        
        # Spara backup
        with open(backup_file, 'w', encoding='utf-8') as f:
            json.dump(backup_data, f, indent=2, ensure_ascii=False)
            
        print(f"‚úÖ Backup skapad: {backup_file}")
        print(f"üîë S√§kerhetskopierade {len(api_keys)} API-nycklar:")
        for key in api_keys.keys():
            print(f"   - {key}")
            
        # Skapa ocks√• en human-readable backup
        readable_backup = self.backup_dir / f"api_keys_readable_{timestamp}.txt"
        with open(readable_backup, 'w', encoding='utf-8') as f:
            f.write(f"# API Keys Backup - {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
            f.write(f"# M√§nniska Maskin Milj√∂ Podcast Project\n\n")
            for key, value in api_keys.items():
                f.write(f"{key}={value}\n")
                
        print(f"üìÑ L√§sbar backup: {readable_backup}")
        return backup_file
    
    def list_backups(self):
        """Lista alla tillg√§ngliga backupper"""
        print("üìã TILLG√ÑNGLIGA API-NYCKEL BACKUPPER")
        print("=" * 50)
        
        backups = list(self.backup_dir.glob("api_keys_backup_*.json"))
        backups.sort(reverse=True)  # Nyaste f√∂rst
        
        if not backups:
            print("‚ùå Inga backupper hittades")
            return []
            
        for i, backup in enumerate(backups, 1):
            try:
                with open(backup, 'r', encoding='utf-8') as f:
                    data = json.load(f)
                timestamp = data.get('datetime', 'Unknown')
                key_count = len(data.get('api_keys', {}))
                print(f"{i}. {backup.name}")
                print(f"   Skapad: {timestamp}")
                print(f"   API-nycklar: {key_count}")
                print()
            except Exception as e:
                print(f"{i}. {backup.name} (fel vid l√§sning: {e})")
                
        return backups
    
    def restore_from_backup(self, backup_file=None):
        """√Öterst√§ll API-nycklar fr√•n backup"""
        print("üîÑ √ÖTERST√ÑLLA API-NYCKLAR FR√ÖN BACKUP")
        print("=" * 50)
        
        if backup_file is None:
            backups = self.list_backups()
            if not backups:
                return
                
            try:
                choice = int(input("V√§lj backup nummer att √•terst√§lla fr√•n: ")) - 1
                if 0 <= choice < len(backups):
                    backup_file = backups[choice]
                else:
                    print("‚ùå Ogiltigt val")
                    return
            except ValueError:
                print("‚ùå M√•ste vara ett nummer")
                return
        
        # L√§s backup
        try:
            with open(backup_file, 'r', encoding='utf-8') as f:
                backup_data = json.load(f)
        except Exception as e:
            print(f"‚ùå Kunde inte l√§sa backup: {e}")
            return
            
        api_keys = backup_data.get('api_keys', {})
        if not api_keys:
            print("‚ùå Inga API-nycklar i backup")
            return
            
        # Bekr√§fta √•terst√§llning
        print(f"üì¶ Backup fr√•n: {backup_data.get('datetime', 'Unknown')}")
        print(f"üîë Inneh√•ller {len(api_keys)} API-nycklar:")
        for key in api_keys.keys():
            print(f"   - {key}")
            
        confirm = input("\nVill du √•terst√§lla dessa nycklar till .env? (j/N): ")
        if confirm.lower() not in ['j', 'ja', 'y', 'yes']:
            print("‚ùå Avbr√∂t √•terst√§llning")
            return
            
        # Backup nuvarande .env f√∂rst
        if self.env_file.exists():
            backup_current = self.env_file.parent / f".env.backup_{datetime.now().strftime('%Y%m%d_%H%M%S')}"
            shutil.copy2(self.env_file, backup_current)
            print(f"üíæ S√§kerhetskopierade nuvarande .env till {backup_current}")
        
        # L√§s nuvarande .env f√∂r att beh√•lla icke-API inst√§llningar
        current_env = {}
        if self.env_file.exists():
            with open(self.env_file, 'r', encoding='utf-8') as f:
                lines = f.readlines()
        else:
            lines = []
            
        # Skriv ny .env med √•terst√§llda API-nycklar
        with open(self.env_file, 'w', encoding='utf-8') as f:
            f.write(f"# M√§nniska Maskin Milj√∂ - √Öterst√§lld fr√•n backup\n")
            f.write(f"# √Öterst√§lld: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
            f.write(f"# Fr√•n backup: {backup_data.get('datetime', 'Unknown')}\n\n")
            
            # Skriv alla API-nycklar fr√•n backup
            for key, value in api_keys.items():
                f.write(f"{key}={value}\n")
                
            f.write("\n# Andra inst√§llningar fr√•n .env.local template\n")
            f.write("USE_GOOGLE_CLOUD_TTS=true\n")
            f.write("GOOGLE_APPLICATION_CREDENTIALS=./google-cloud-service-account.json\n")
            f.write("PODCAST_TITLE=M√§nniska Maskin Milj√∂\n")
            f.write("PODCAST_AUTHOR=Pontus - M√§nniska Maskin Milj√∂\n")
            f.write("PODCAST_EMAIL=podcast@example.com\n")
            f.write("PODCAST_LANGUAGE=sv-SE\n")
            f.write("LOCAL_TEST_MODE=false\n")
            f.write("DEBUG_MODE=false\n")
            
        print(f"‚úÖ API-nycklar √•terst√§llda till .env")
        print(f"üîë √Öterst√§llde {len(api_keys)} API-nycklar")
        
    def verify_keys(self):
        """Verifiera att alla n√∂dv√§ndiga API-nycklar finns"""
        print("üîç VERIFIERAR API-NYCKLAR")
        print("=" * 50)
        
        if not self.env_file.exists():
            print("‚ùå .env fil saknas!")
            return False
            
        # L√§s .env
        env_vars = {}
        with open(self.env_file, 'r', encoding='utf-8') as f:
            for line in f:
                line = line.strip()
                if line and not line.startswith('#') and '=' in line:
                    key, value = line.split('=', 1)
                    env_vars[key.strip()] = value.strip()
        
        # Obligatoriska nycklar
        required_keys = [
            'OPENROUTER_API_KEY',
            'CLOUDFLARE_API_TOKEN',
            'CLOUDFLARE_R2_ACCOUNT_ID',
            'CLOUDFLARE_R2_BUCKET'
        ]
        
        # Google Cloud eller ElevenLabs kr√§vs
        tts_keys = [
            'GOOGLE_APPLICATION_CREDENTIALS',
            'ELEVENLABS_API_KEY'
        ]
        
        missing = []
        present = []
        
        # Kontrollera obligatoriska
        for key in required_keys:
            if key in env_vars and env_vars[key] and env_vars[key] != f'your_{key.lower()}_here':
                present.append(key)
                print(f"‚úÖ {key}")
            else:
                missing.append(key)
                print(f"‚ùå {key} - SAKNAS")
        
        # Kontrollera TTS
        has_tts = False
        for key in tts_keys:
            if key in env_vars and env_vars[key] and env_vars[key] != f'your_{key.lower()}_here':
                present.append(key)
                print(f"‚úÖ {key} (TTS)")
                has_tts = True
        
        if not has_tts:
            print("‚ùå Ingen TTS provider konfigurerad (Google Cloud eller ElevenLabs)")
            missing.extend(tts_keys)
            
        print(f"\nüìä RESULTAT:")
        print(f"‚úÖ Konfigurerade: {len(present)}")
        print(f"‚ùå Saknade: {len(missing)}")
        
        if missing:
            print(f"\nüö® √ÖTG√ÑRD KR√ÑVS:")
            print("Du kan:")
            print("1. K√∂ra 'python api_key_backup_manager.py restore' f√∂r att √•terst√§lla fr√•n backup")
            print("2. Manuellt l√§gga till saknade nycklar i .env")
            
        return len(missing) == 0
    
    def _verify_git_ignore_security(self):
        """Verifiera att k√§nsliga filer ignoreras av git"""
        import subprocess
        
        try:
            # Testa om .env ignoreras
            result = subprocess.run(
                ['git', 'check-ignore', '.env'],
                capture_output=True, text=True, cwd=self.project_root
            )
            
            if result.returncode != 0:
                print("üö® S√ÑKERHETSVARNING: .env filen ignoreras INTE av git!")
                print("‚ö†Ô∏è  Risk: API-nycklar kan l√§cka till GitHub!")
                print("üîß √Ötg√§rd: Kontrollera .gitignore filen")
                return False
                
            # Testa om backup-mappen ignoreras  
            test_backup = self.backup_dir / "test.json"
            test_backup.parent.mkdir(parents=True, exist_ok=True)
            test_backup.write_text("{}")
            
            result = subprocess.run(
                ['git', 'check-ignore', str(test_backup)],
                capture_output=True, text=True, cwd=self.project_root
            )
            
            test_backup.unlink()  # Ta bort testfil
            
            if result.returncode != 0:
                print("üö® S√ÑKERHETSVARNING: Backup-filer ignoreras INTE av git!")
                print("‚ö†Ô∏è  Risk: API-nycklar kan l√§cka till GitHub!")
                print("üîß √Ötg√§rd: Uppdatera .gitignore med 'backups/**'")
                return False
                
            print("üõ°Ô∏è Git s√§kerhet: K√§nsliga filer skyddade ‚úÖ")
            return True
            
        except Exception as e:
            print(f"‚ö†Ô∏è Kunde inte verifiera git s√§kerhet: {e}")
            print("üîß Kontrollera manuellt att .env och backups/ finns i .gitignore")
            return False

